def normalize_lang(code: str | None) -> str:
    if not code:
        return _DEFAULT_LANG
    code = str(code).strip().lower().replace("_", "-").split("-")[0]
    known = {c for c, _ in UI_LANGUAGES}
    return code if code in known else _DEFAULT_LANG


def tr(text: str, lang: str | None = None) -> str:
    """Translate menu text; English key is returned when no translation exists."""
    if not text:
        return text
    lang = normalize_lang(lang)
    if lang == _DEFAULT_LANG:
        return text
    return _TRANSLATIONS.get(lang, {}).get(text, text)


def language_label(code: str) -> str:
    for c, label in UI_LANGUAGES:
        if c == normalize_lang(code):
            return label
    return "English"
